-- Auto-bootstrap Packer on fresh install
-- This ensures Packer plugin manager is installed automatically when you first open nvim.
-- If Packer is not found, it will be cloned from GitHub and installed.
-- After installation, PackerSync will run automatically to install all plugins.
local ensure_packer = function()
  local fn = vim.fn
  local install_path = fn.stdpath('data')..'/site/pack/packer/start/packer.nvim'
  if fn.empty(fn.glob(install_path)) > 0 then
    print("Installing Packer plugin manager...")
    fn.system({'git', 'clone', '--depth', '1', 'https://github.com/wbthomason/packer.nvim', install_path})
    vim.cmd [[packadd packer.nvim]]
    return true
  end
  return false
end

local packer_bootstrap = ensure_packer()

-- Only required if you have packer configured as `opt`
vim.cmd [[packadd packer.nvim]]

